    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JOptionPane.showConfirmDialog(MainMenu.main, "Are you sure you"
                + "want to replace '" + valuesModel.getElementAt(indx1) + "' "
                + "with '" + newValue.getText() + "' ?") 
                == JOptionPane.YES_OPTION) {
            Change temp = new Change(valuesModel.getElementAt(indx1).toString(), newValue.getText());
            changes.add(temp);
            curvalues[indx1] = null;
            int dist = 0;
            for (int i = 0; i < curvalues.length; i++) {
                if (curvalues[i] == null) {
                    dist++;
                } else {
                    curvalues[i - dist] = curvalues[i];
                    curvalues[i] = null;
                }
            }
            load();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

