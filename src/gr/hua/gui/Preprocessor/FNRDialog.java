    private void load() {
        valuesModel.removeAllElements();
        replacementsModel.removeAllElements();
        for (String s : curvalues) {
            if (s == null) {
                continue;
            }
            valuesModel.addElement(s);
            replacementsModel.addElement(s);
        }
        if (!replacementsModel.isEmpty()) {
            if (indx2 > replacementsModel.getSize()) {
                replacementsL.setSelectedIndex(replacementsModel.getSize() - 1);
            } else if (indx2 != -1) {
                replacementsL.setSelectedIndex(indx2);
            } else {
                replacementsL.setSelectedIndex(0);
            }
        }
        if (!valuesModel.isEmpty()) {
            if (indx1 > valuesModel.getSize()) {
                valuesL.setSelectedIndex(valuesModel.getSize() - 1);
            } else if (indx1 != -1) {
                valuesL.setSelectedIndex(indx1);
            } else {
                valuesL.setSelectedIndex(0);
            }
        }
    }
    
    public LinkedList<Change> open() {
        setVisible(true);
        return changes;
    }
    
